# Operações lógicas:
x = 1;
y = 2;
# Igualdade: ==
puts true == false
# Desigualdade: !=
puts true != false
# Maior que: >
puts x > y
# Menor que: <
puts x < y
# Maior ou igual a: >=
puts x >= y
# Menor ou igual a: <=
puts x <= y
# E lógico: && ou and
puts true && false
# Ou lógico: || ou or
puts true || false
# Negação lógica: ! ou not,
puts !true
